public class Variables {
    public static void main(String[] args) {
        String breed = "t-rex";
        double height = 10.2;
        double length = 15.9;
        int weight = 6000;

        System.out.println("Breed: " + breed);
        System.out.println("Height:" + height +"m");
        System.out.println("Length:" + length +"m");
        System.out.println("Weight:" + weight +"kg");

        boolean carnivore = true;
        int age = 26;
        String name = "John";

        System.out.println("Carnivore: "+ carnivore);
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);

        int maximumNumberOfVistors = 200;
        System.out.println("There's a maximum of " + maximumNumberOfVistors + " people allowed in Mesozoic Eden");

        int employeeAge = 37;
        String employeeName = "Dave";

        System.out.println("Employee name: " + employeeName + " Employee age: " + employeeAge);

        int numberOfDinos = 26;
        System.out.println("We have : " + numberOfDinos + " dinos");

        int safetyRating = 9;
        System.out.println("Our safety rating is : " + safetyRating);
        System.out.println();

    }
}
